export function marketplaceCountryFlag(isoCode: string): string {
  const code = isoCode.toUpperCase();
  if (code.length !== 2) return "🏳️";
  return String.fromCodePoint(...[...code].map((char) => 127397 + char.charCodeAt(0)));
}

